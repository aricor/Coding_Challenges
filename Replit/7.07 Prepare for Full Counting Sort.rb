def full_counting_sort_prep(array)    
    array.map!{ |e|  e.split.first.to_i} 
    count = Array.new(array.max+1,0)
    array.each{ |e| count[e] += 1 }
    for i in 1..count.length-1
        count[i] += count[i-1]
    end 
    count
end
  
